	private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {
		// TODO add your handling code here:

		String tic = txtCodigo.getText();

        ENTicket t  = new ENTicket();
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
              t.setTiCodigo(txtCodigo.getText());
              gestionParqueadero.buscarTicK(t);
              txtCodigo.setText(gestionParqueadero.buscarTicK(t).getTiCodigo());
              txthoraGuardada.setText(gestionParqueadero.buscarTicK(t).getTiHoraIngreso());
              String a = gestionParqueadero.buscarTicK(t).getTiHoraIngreso();
              String b = txtHora.getText();
              gestionParqueadero.calcularTiempo(a, b);
              txtTiempo.setText(String.valueOf(gestionParqueadero.calcularTiempo(a, b)));
              txtCosto.setText(String.valueOf(gestionParqueadero.valorPagar(gestionParqueadero.calcularTiempo(a, b))));
		
		
	}
	}

